export default async function Home() {
  // Require authentication - redirects to /login if not authenticated
  const user = await requireAuth()

  // Customers should not access dashboard - redirect to public order page
  if (user.profile?.role === 'customer') {
    redirect('/order')
  }

  // Filter features based on user role
  const visibleFeatures = features.filter((feature) => {
    const role = user.profile?.role

    // Admin sees all features
    if (role === 'admin') return true

    // Staff only sees Recipe Book
    if (role === 'staff') {
      return feature.id === 'recipe-book'
    }

    // Default: hide all (shouldn't reach here due to customer redirect above)
    return false
  })

  return (
    <div className="min-h-screen bg-gradient-to-br from-gray-50 to-gray-100">
      {/* Header */}
      <header className="bg-white shadow-sm">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
          <div className="text-center">
            <h1 className="text-4xl font-bold text-gray-900 mb-2">GAJA</h1>
            <p className="text-lg text-gray-600">
              Your all-in-one restaurant management platform
            </p>
          </div>
        </div>
      </header>

      {/* Main Content */}
      <main className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-12">
        <div className="mb-8">
          <h2 className="text-2xl font-semibold text-gray-800 mb-2">
            Features
          </h2>
          <p className="text-gray-600">Choose a feature to get started</p>
        </div>

        {/* Feature Cards Grid */}
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {visibleFeatures.map((feature) => (
            <FeatureCard
              key={feature.id}
              title={feature.title}
              description={feature.description}
              route={feature.route}
              icon={feature.icon}
            />
          ))}
        </div>
      </main>

      {/* Footer */}
      <footer className="mt-16 py-8 border-t border-gray-200">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 text-center">
          <p className="text-gray-500 text-sm">
            Built with Next.js and Tailwind CSS
          </p>
        </div>
      </footer>
    </div>
  )
}
